/***********************************************************************
Write a function `countScores(people)` that takes in an array of score
objects (people) as its input. A score object has two key-value pairs:
the scorer (string) and a point value (number). `countScores(people)` should
return an object that has key-value pairs listing each person who scored as a key
and the sum of the total points for the game as their value.


Example 1:
let ppl = [{name: "Anthony", score: 10},
            {name: "Fred", score : 10},
            {name: "Anthony", score: -8},
            {name: "Winnie", score: 12}];

console.log(countScores(ppl)); //=> { Anthony: 2, Fred: 10, Winnie: 12 }

Example 2:
let peeps = [
  {name: "Anthony", score: 2},
  {name: "Winnie", score: 2},
  {name: "Fred", score: 2},
  {name: "Winnie", score: 2},
  {name: "Fred", score: 2},
  {name: "Anthony", score: 2},
  {name: "Winnie", score: 2}
];
console.log(countScores(peeps)); //=> { Anthony: 4, Fred: 4, Winnie: 6 }
***********************************************************************/

function countScores(people) {
  // Your code here
  //1. Declare an empty object
  let scoreBoard = {};
  //2. Iterate through the array
  for (let i = 0; i < people.length; i++){
      //2a. Destructure alias each object in the array so you have access to the key and value
      let person = people[i];
      let { name: firstName, score: currentScore } = person;

      if (scoreBoard[firstName] === undefined){
        scoreBoard[firstName] = currentScore;
      } else {
        scoreBoard[firstName] += currentScore;
      }
  }

  //3. If the name is not in obj yet, add the prop and add their initial score
  //4. If the name is in the obj already, just add the score to the accumulated score
  //5. Return the obj

  return scoreBoard;
}

